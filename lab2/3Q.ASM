	DCD 0x40001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
 LDR R1,=SRC
 LDR R2,=DST
 LDR R3,=ANS
 MOV R5,#1
 MOV R6,#0
 LDR R1,[R1]
 LDR R2,[R2]
 SUBS R6,R1,R2
 SBCS R5,#0
 STR R5,[R3],#4
 STR R6,[R3]
 STOP B STOP
SRC DCD 0x33332222
DST DCD 0x22222222
 AREA DATA1, DATA, READWRITE
ANS DCD 0
END