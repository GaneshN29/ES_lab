	DCD 0X10001000 
	DCD Reset_Handler 
	ALIGN 
	AREA MYCODE,CODE,READONLY 
	ENTRY
	EXPORT Reset_Handler 
Reset_Handler 
	LDR R0,=SRC1
	LDR R7,=SRC2
	LDR R1,=DST
	MOV R3,#4
UP	LDR R4, [R0],#4
	LDR R8, [R7],#4
	ADD R4, R8
	ADC R6,#0
	SUBS R3,#01
	BNE UP
	STR R5,[R1],#4
	STR R6,[R1]
STOP
	B STOP
	
SRC1 DCD 0xFFFFFFFF, 0XFFFFFFFF, 0XFFFFFFFF, 0XFFFFFFFF
SRC2 DCD 0xFFFFFFFF, 0XFFFFFFFF, 0XFFFFFFFF, 0XFFFFFFFF
    AREA MYDATA, DATA, READWRITE 
DST DCD 0
	END
	